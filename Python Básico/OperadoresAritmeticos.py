#  Operadores Aritméticos 
""" 
+ - Adição
- - Subtração
* - Multiplicação
/ - Divisão com Resultado de Ponto Flutuante 
//- Divisão por Inteiro o resultado desta divisão é arredondado para um número inteiro.
**- Exponenciação Um número elevado a outro
% - Resto: Também conhecido como módulo/ Retorna o resto da divisão entre um número e outro
()- Serve para alterar a precedência/condição do que, por importância, deve estar em primeiro lugar; 
preferência, primazia, prioridade.
"""
# Detalhes
print('Multiplicação *', 10 * '10')  #O Operador de Multiplicação, também serve para repetições.

print(10.5 //3)  #Divisão por Inteiros: Vai retornar 3.0
print(10 //3)  #Divisão por Inteiros: Vai retornar 3 por que são dois inteiros
print(10 / 3) #Divisão Normal: Vai me retornar o valor real daquela divisão,porém com um baita ponto flutuante
print(2**10) #Potenciação: Estou falando que é 2 elevado a 10
print(10 % 5)  # Irá me retornar o resto desta divisão que é 0 